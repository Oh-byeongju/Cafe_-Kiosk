CREATE TABLE 음료(
    이름  VARCHAR(20) NOT NULL,
    가격  INT  NOT NULL,
    카페인 VARCHAR(20) NOT NULL,
    종류 VARCHAR(20),
    등록일 DATE,
    CONSTRAINT ck_Caffeine1 CHECK ( 카페인 = '무'  OR 카페인 = '유'),
    PRIMARY KEY(이름)
);

CREATE TABLE 주문기록(
    주문번호 INT,
    주문일시 DATE,
    합계 INT,
    포장여부 VARCHAR(10),
    PRIMARY KEY(주문번호)
);

CREATE TABLE 음료_주문기록(
    이름 VARCHAR(20),
    주문번호 INT,
    수량 INT,
    시가 INT,
    CONSTRAINT PRI_KEY PRIMARY KEY(이름, 주문번호),
    CONSTRAINT FOR_KEY FOREIGN KEY(이름) REFERENCES 음료(이름),
    CONSTRAINT FOR_KEY2 FOREIGN KEY(주문번호) REFERENCES 주문기록(주문번호)
);
   
